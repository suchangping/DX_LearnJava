package chapter01;

public class Structure {
    public static void main(String[] args) {
        double pi = 3.14;
        int pie = 5;
        System.out.println("半径" + pie + "cmのパイの面積は、");
        System.out.println(pie * pie * pi);

        pi = 10;
        System.out.println("半径" + pie + "cmのパイの面積は、");
        System.out.println(pie * pie * pi);
    }
}
